#include <bits/stdc++.h>
using namespace std;
int main()
{
    string s;
    getline(cin,s);
   
    for (char i = 'a'; i <= 'z'; i++)
    {
        int ch = 0;
        for (char x : s)
        {
            if (x == i)
            {
                ch = 1;
                break;
            }
        }
        if (!ch)
        {
            cout << i << endl;
            
            return 0;
        }
    }
    cout << "None" << endl;
    return 0;
}





// এই কোডটি স্ট্রিং এর প্রতিটি অক্ষরের জন্য চেক করে যে কোন অক্ষর ব্যবহার করা হয়েছে কিনা। যদি অক্ষর ব্যবহার না হয় তাহলে সেই অক্ষরটি প্রিন্ট করে প্রোগ্রাম থেকে বাহির হয়ে যায়। যদি সব অক্ষর ব্যবহার হয়ে থাকে তাহলে "None" প্রিন্ট করা হয়।

// কোডের ব্যাখ্যা:

// getline(cin, s);: ইনপুট স্ট্রিং নেওয়া।
// for (char i = 'a'; i <= 'z'; i++): সব লেটারের জন্য লুপ চালানো।
// int ch = 0;: শুরুতেই সব অক্ষরের জন্য একটি ফ্লাগ তৈরি করা।
// for (char x : s): স্ট্রিং এর প্রতিটি অক্ষরের জন্য চেক করা।
// if (x == i): যদি অক্ষর মিলে:
// ch = 1;: ফ্লাগকে ১ দেওয়া।
// break;: লুপ থেকে বাহির হয়ে যাওয়া।
// if (!ch): যদি কোন ফ্লাগ সেট না থাকে তাহলে:
// cout << i << endl;: অক্ষরটি প্রিন্ট করা।
// return 0;: প্রোগ্রাম থেকে বাহির হয়ে যাওয়া।
// লুপ শেষে cout << "None" << endl; দিয়ে "None" প্রিন্ট করা হয়।
// এই কোডটি প্রথম অক্ষরটি প্রিন্ট করে এবং প্রোগ্রাম থেকে বাহির হয়ে যায়। এটি সব অক্ষর প্রকাশ করার জন্য যোগাযোগ করেনা।